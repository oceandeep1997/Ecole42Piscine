int	ft_iterative_factorial(int nb)
{
	int	prod;

	prod = 1;
	if (nb < 0 || nb > 12)
		return (0);
	if (nb == 0)
		return (1);
	while (nb > 0)
	{
		prod *= nb;
		nb--;
	}
	return (prod);
}
